    private void btnCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcActionPerformed
        // TODO add your handling code here:
        try{
            double peso = Double.parseDouble(txtPeso.getText());
            if (peso < 0)
                throw new Exception();
            double altura = Double.parseDouble(txtAltura.getText());
            if (altura < 0)
                throw new Exception();
               
            double imcValue = imc(peso, altura);
            
            if (imcValue < 17)
                JOptionPane.showMessageDialog(null, "Muito abaixo do peso\n");
            else if (imcValue < 18.5)
                JOptionPane.showMessageDialog(null, "Abaixo do peso\n");
            else if (imcValue < 25)
		JOptionPane.showMessageDialog(null, "Peso normal");
            else if (imcValue < 30)
                JOptionPane.showMessageDialog(null, "Acima do peso");
            else if (imcValue < 34.9)
		JOptionPane.showMessageDialog(null, "Obesidade grau I");
            else if (imcValue < 40)
		JOptionPane.showMessageDialog(null, "Obsidade grau II");
            else
		JOptionPane.showMessageDialog(null, "Obsidade grau III");
            
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "ERRO", "Informe um valor vÃ¡lido", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCalcActionPerformed

