    private void btnConverteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConverteActionPerformed
        // TODO add your handling code here:
        try{
            double temp = Double.parseDouble(txtValor.getText());
            //int op = Integer.parseInt(cbTemp.toString());
            int op = cbTemp.getSelectedIndex();
            
            if (op == 0)
                selCelsius(temp);
            if (op == 1)
                 selFah(temp);
            if (op == 2)
                selKelvin(temp);    
        }catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Informe um valor válido", "erro", JOptionPane.ERROR_MESSAGE);
        }    
    }//GEN-LAST:event_btnConverteActionPerformed

