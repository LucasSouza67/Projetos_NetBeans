    public void selCelsius(double temp){
        double k = temp + 273.15; 
        double f = (temp * 9/5) + 32;
        StringBuilder resultado = new StringBuilder();
        resultado.append("Kelvin: ").append(String.format("%.2f K", k)).append("\n");
        resultado.append("Fahrenheit: ").append(String.format("%.2f °F", f));
        JOptionPane.showMessageDialog(null, resultado);
    }
    
    public void selKelvin(double temp){
        double c = temp - 273.15;
        double f = (c * 9/5) + 32;
        StringBuilder resultado = new StringBuilder();
        resultado.append("Celsius: ").append(String.format("%.2f °C", c)).append("\n");
        resultado.append("Fahrenheit: ").append(String.format("%.2f °F", f));
        JOptionPane.showMessageDialog(null, resultado);
    }
    
    public void selFah(double temp){
        double c = (temp - 32) * 5/9;
        double k = c + 273.15;
        StringBuilder resultado = new StringBuilder();
        resultado.append("Celsius: ").append(String.format("%.2f °C", c)).append("\n");
        resultado.append("Kelvin: ").append(String.format("%.2f K", k));
        JOptionPane.showMessageDialog(null, resultado);
    }
    
